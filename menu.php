<br><h3><center>Apa Itu Sumpah Pemuda?</center></h3>
        <p><center>Sumpah Pemuda adalah salah satu peristiwa penting dalam sejarah Indonesia yang terjadi pada 28 Oktober 1928. Peristiwa ini merupakan momentum yang sangat berarti dalam perjuangan kemerdekaan Indonesia, karena melalui Sumpah Pemuda, terbentuklah semangat persatuan dan kesatuan bangsa Indonesia.
Pada tahun 1928, Indonesia masih berada di bawah kekuasaan kolonial Belanda. Pada tanggal 28 Oktober 1928, sekitar 100 pemuda dari berbagai organisasi pemuda di Jakarta, seperti Jong Java, Jong Sumatra, dan Jong Batak, berkumpul di Gedung Katholieke Jongenlingen Bond (KJB) di Jakarta. Mereka mengadakan Kongres Pemuda II, yang bertujuan untuk membahas dan menyampaikan aspirasi pergerakan pemuda Indonesia.
Pada Kongres Pemuda II inilah terjadi peristiwa penting yang kemudian dikenal dengan sebutan Sumpah Pemuda. Dalam sidang pleno yang dihadiri oleh para pemuda perwakilan dari seluruh Indonesia, disampaikan tiga butir sumpah yang kemudian menjadi semangat perjuangan bangsa Indonesia.</center></p>
