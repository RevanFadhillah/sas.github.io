<p>Soenario Sastrowardoyo</p>
Prof Mr Soenario Sastrowardoyo merupakan pemuda asal Madiun, Jawa Timur. Soenario berperan sebagai penasehat panitia dalam merumuskan Sumpah Pemuda dan pembicaranya.</p>
<p>Amir Syarifuddin Harahap</p>
Amir merupakan tokoh pemuda dari organisasi Jong Batak Bond dan aktivis yang sangat anti Jepang. Amir berperan sebagai Bendahara Kongres Pemuda II. Amir juga memberikan ide-ide brilian saat perumusan sumpah pemuda.</p>
<p>Mohammad Yamin</p>
Mohammad Yamin adalah pemuda asal tanah Minangkabau dan tokoh Jong Sumatranen Bond. Dia juga terkenal sebagai sastrawan, sejarawan, budayawan, politikus, dan ahli hukum. M Yamin memberikan usulan agar Bahasa Indonesia menjadi bahasa persatuan dalam ikrar sumpah pemuda.</p>
<p>Djoko Marsaid</p>
Djoko Marsaid adalah tokoh pemuda dari Jong Java. Pada Kongres Pemuda II, Djoko Marsaid berperan sebagai wakil ketua Kongres Pemuda II.</p>
<p>Soegondo Djojopoespito</p>
Soegondo Djojopoespito berperan sebagai ketua yang memimpin jalannya kegiatan Kongres Pemuda II. Soegondo terpilih menjadi Ketua Kongres atas persetujuan Mohammad Hatta, karena ia adalah anggota Persatuan Pemuda Indonesia (PPI)</p>
<p>Johannes Leimena</p>
Johannes Leimena adalah panitia Kongres pemuda I dan II. J Leimena merupakan pemuda dari Jong Ambon yang berprofesi sebagai dokter dan politisi.</p>
<p>Sarmidi Mangoensarkoro</p>
Sarmidi Mangoensarkoro merupakan aktivis pendidikan. Dia banyak berbicara soal pendidikan untuk rakyat Indonesia saat Kongres Pemuda I dan II. Bahkan, Sarmidi juga dipercaya untuk menjadi menteri pendidikan dan kebudayaan Indonesia pada tahun 1949 sampai 1950.</p>
<p>Johan Mohammad Cai</p>
Johan berperan sebagai Pembantu I di Kepanitiaan Kongres Pemuda. Dia adalah peranakan Tionghoa yang terlibat aktif di Kongres Pemuda II.</p>
<p>Rumondor Cornelis Lefrand Senduk
Senduk adalah dokter dan politikus Indonesia asal Minahasa, Sulawesi Utara. Dia merupakan anggota Jong Celebes yang berperan sebagai Pembantu III pada Kongres Pemuda II.</p>
<p>Mohammad Rochjani Su’ud</p>
Rochjani Su’ud merupakan ahli hukum sekaligus Ketua Pemoeda Betawi. Dia berperan sebagai Pembantu V dalam Kongres Pemuda II.</p>
<p>R Katja Soengkana</p>
Katja Soengkana menjadi Pembantu II di kepanitian Kongres Pemuda II. Dia merupakan perwakilan dari organisasi Pemoeda Indonesia atau Jong Indonesie.</p>
<p>Wage Rudolf Soepratman</p>
WR Soepratman merupakan seorang wartawan, violinis, sekaligus komponis adal Indonesia. Dia menciptakan lagu Indonesia Raya dan memainkan lagu tersebut di hadapan peserta Kongres Pemuda II tanpa teks dengan biola untuk pertama kalinya.</p>
<p>Theodora Athia Salim (Dolly Salim)</p>
Dolly Salim juga merupakan salah satu tokoh penting Sumpah Pemuda meski bukan anggota Kongres Pemuda II. Putri dari Agus Salim inilah yang melantunkan lagu Indonesia Raya untuk pertama kali.</p>
